import type { iBaseApiResponse } from './_baseApiResponse.interface';

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// CREATE CATEGORY
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
export interface iCreateCategoryData {
	category: {
		title: string;
	};
}

export interface iCreateCategoryResponse extends iBaseApiResponse {
	result: {
		message: string;
		category: {
			id: string;
			title: string;
		};
	};
}

export interface iCreateCategoryResult {
	id: string;
	title: string;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// GET ALL CATEGORIES
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
export interface iGetAllCategoriesResponse extends iBaseApiResponse {
	result: {
		message: string;
		categories: { id: string; title: string }[];
	};
}

export interface iGetAllCategoriesResult {
	id: string;
	title: string;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// DELETE CATEGORY
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
export interface iDeleteCategoryData {
	id: string;
}

export interface iDeleteCategoryResponse extends iBaseApiResponse {
	result: string;
}

export interface iDeleteCategoryResult {
	message: string;
}

