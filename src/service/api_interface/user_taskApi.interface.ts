import type { iBaseApiResponse } from './_baseApiResponse.interface';

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// ASSIGN TASK TO USER
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
export interface iAssignTaskToUserData {
	task_id: string;
	user_id: string;
}

export interface iAssignTaskToUserResponse extends iBaseApiResponse {
	result: string;
}

