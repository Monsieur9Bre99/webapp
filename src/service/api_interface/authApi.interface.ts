import type { iBaseApiResponse } from './_baseApiResponse.interface';

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// SIGNUP USER
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
export interface iSignupUserData {
	firstname: string;
	lastname: string;
	email: string;
	username: string;
	password: string;
}

export interface iSignupUserResponse extends iBaseApiResponse {
	result: string;
}

export interface iSignupUserResult {
	message: string;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// SIGNIN USER
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
export interface iSigninUserData {
	email: string;
	password: string;
	rememberMe: boolean;
}

export interface iSigninUserResponse extends iBaseApiResponse {
	result: {
		message: string;
		authentification: {
			accessToken: string;
			userId: string;
		};
	};
}

export interface iSigninUserResult {
	authentification: {
		accessToken: string;
		userId: string;
	};
}

